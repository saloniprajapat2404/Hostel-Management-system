export function t(lang, key, vars = {}) {
  let text = translations[lang]?.[key] ?? translations.en[key] ?? key
  for (const [name, value] of Object.entries(vars)) {
    text = text.replaceAll(`{{${name}}}`, value ?? '')
  }
  return text
}
